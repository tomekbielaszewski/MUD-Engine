package org.grizz.game.model;

public class Player {
    private Object location;
    private Object equipment;
    private Object backpack;
    private String name;

    public Object getLocation() {
        return location;
    }

    public Object getEquipment() {
        return equipment;
    }

    public Object getBackpack() {
        return backpack;
    }

    public String getName() {
        return name;
    }
}
